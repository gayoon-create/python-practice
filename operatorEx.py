# 파이썬에서 사용 가능한 연산자

# 1) 산술 연산자 : +, -, *, /, %, **(거듭제곱), //(몫)
print(3**2) #9
print(10 // 3) #3


# 2) 대입 연산자 : +=, -=, *=, /=, %=, **=, //=

# 3) 관계 연산자 : >, <, >=, <=, ==(equal), !=(not equal)

# 4) 논리 연산자 : and, or, not
print(True and False) # False 둘 다 True여야 True
print(True or False) # True 둘 중 하나만 True여도 True

# 5) Bitwise 연산자 : &(비트 연산자 and), |(비트 연산자 or), ~(비트 연산자 not), ^(비트 연산자 xor), <<, >>(비트 쉬프트 연산자)
